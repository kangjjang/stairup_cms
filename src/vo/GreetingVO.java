package vo;

public class GreetingVO {
	int greetingSeqNo;
	
	String greetingContent;
	String crtDate;
	public int getGreetingSeqNo() {
		return greetingSeqNo;
	}
	public void setGreetingSeqNo(int greetingSeqNo) {
		this.greetingSeqNo = greetingSeqNo;
	}
	public String getGreetingContent() {
		return greetingContent;
	}
	public void setGreetingContent(String greetingContent) {
		this.greetingContent = greetingContent;
	}
	public String getCrtDate() {
		return crtDate;
	}
	public void setCrtDate(String crtDate) {
		this.crtDate = crtDate;
	}
	
	
}
